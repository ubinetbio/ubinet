<h2>UbiNet version 1.0: Data statistics</h2>
<hr />
	<p align=justify><strong class="style1">UbiNet</strong> is a web-based system that provides a comprehensive collection of available ubiquitinated substrate proteins, ligating enzyme E3,  E3-substrate specificities and a systematic framework for the analysis of E3 ligase regulatory networks in protein ubiquitination, focusing on <span class="style8">Human</span> organism. Currently,&nbsp;<strong>UbiNet</strong>&nbsp;contains&nbsp;499  E3s&nbsp;and&nbsp;14692 ubiquintinated substrate proteins&nbsp;in Human,  and&nbsp;10437 E3-substrate specific relations&nbsp;between&nbsp;438  E3s&nbsp;and&nbsp;2839 ubiquitinated substrate proteins. In addition, 2143  records of E3-associated functional categories (including pathway, cellular  process, molecular function, cellular localization, protein complex, protein  family, and disease) were collected serving for the analysis.</p>
